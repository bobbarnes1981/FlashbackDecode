namespace Decoder
{
    /// <summary>
    /// Condition flag enum.
    /// </summary>
    public enum Condition
    {
        /// <summary>
        /// True
        /// </summary>
        T,

        /// <summary>
        /// False
        /// </summary>
        F,

        /// <summary>
        /// High
        /// </summary>
        HI,

        /// <summary>
        /// Low or same
        /// </summary>
        LS,

        /// <summary>
        /// Carry cleared
        /// </summary>
        CC,

        /// <summary>
        /// Carry set
        /// </summary>
        CS,

        /// <summary>
        /// Not equal (zero)
        /// </summary>
        NE,

        /// <summary>
        /// Equal (zero)
        /// </summary>
        EQ,

        /// <summary>
        /// Overflow cleared
        /// </summary>
        VC,

        /// <summary>
        /// Overflow set
        /// </summary>
        VS,

        /// <summary>
        /// Plus
        /// </summary>
        PL,

        /// <summary>
        /// Minus
        /// </summary>
        MI,

        /// <summary>
        /// Greater than or equal to
        /// </summary>
        GE,

        /// <summary>
        /// Less than
        /// </summary>
        LT,

        /// <summary>
        /// Greater than
        /// </summary>
        GT,

        /// <summary>
        /// Less than or equal to
        /// </summary>
        LE,
    }
}
